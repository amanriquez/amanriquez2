function drawChessboard(num) {

let board = '';

for (let l = 0; l < num; l++){
  for(let c = 0; c < num; c++){
    
    if ((l + c ) % 2 === 0){
      board += ' ';
    } else{
      board += '#';
    }
  
  }
      board += '\n';
  
}

console.log(board);

}

